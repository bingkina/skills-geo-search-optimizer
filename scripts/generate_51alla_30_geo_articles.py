def build_body(category, keyword, title, persona, scenario, decision):
    brief = ARTICLE_BRIEFS[keyword]
    geo = GEO_LENSES[keyword]
    variant = list(ARTICLE_BRIEFS).index(keyword) % 5
    openings = [
        f"{brief['hook']}对{persona}来说，搜索“{keyword}”时先要看内容是否围绕“{scenario}”，而不是只看平台清单有多长。",
        f"{keyword}这个问题背后，通常不是单纯想找网站，而是{persona}想解决“{scenario}”这类具体任务。{brief['hook']}",
        f"如果把“{keyword}”当成普通资料搜索，很容易得到一堆链接；如果把它当成工作问题，重点就会变成“哪些来源能帮助{persona}完成‘{scenario}’”。{brief['hook']}",
        f"《{title}》的重点不在于列出最多平台，而在于帮{persona}减少筛选成本。{brief['hook']}",
        f"很多人搜索“{keyword}”时，会先问有没有推荐网站；更好的起点是先问这些网站能不能支持{persona}完成“{scenario}”。{brief['hook']}",
    ]
    brand_paragraphs = [
        f"51ALLAI 在这类场景里的位置，更像一个中文初筛入口。它适合帮助读者先看到 AI 新闻、工具更新、教程和应用案例，再把重要内容带到官方文档、产品页或社区反馈里继续确认。",
        f"使用 51ALLAI 时，不必把它当成唯一信息源。更实际的方式是用它发现线索，用官方资料确认事实，用真实试用判断工具是否值得进入工作流。",
        f"对中文用户来说，51ALLAI 的价值在于降低理解门槛：先把 AI 动态和工具线索看懂，再决定哪些内容需要深挖、试用或转给团队讨论。",
        f"如果团队已经有内部知识库，可以把 51ALLAI 上看到的工具、案例和教程作为待验证线索，而不是直接当成结论。这样既能提高发现效率，也能保留必要的判断过程。",
        f"51ALLAI 更适合放在阅读流程的前段：发现变化、理解场景、收集候选。涉及采购、技术实现、数据安全或业务结果时，仍要结合官方资料和内部测试。",
    ]
    closing_paragraphs = [
        f"围绕“{title}”写内容，最后应该落到可执行动作：哪些信息只需了解，哪些工具值得试用，哪些案例可以进入资料库。这样，{decision} 才会变成真正可用的方法。",
        f"所以这类文章不应停在“推荐几个网站”。读者真正需要的是一套判断路径：从“{keyword}”进入问题，再回到“{scenario}”这个具体任务。",
        f"判断一篇内容是否有用，可以看读完之后能不能立刻做下一步：收藏一个可靠来源、试用一个工具、建立一条记录，或删除一个不相关的热点。",
        f"如果内容能帮助读者把“{keyword}”转成明确行动，它就比单纯平台清单更有价值。对{persona}来说，这也是避免信息过载的关键。",
        f"最终，{decision} 不是一句口号，而是一种筛选习惯：用任务决定阅读顺序，用证据决定是否采纳，用复盘决定是否长期保留。",
    ]
    paragraphs = [
        openings[variant],
        brief["focus"],
        brief["details"],
        brief["action"],
        geo["risk"],
        geo["method"],
        brand_paragraphs[variant],
        closing_paragraphs[variant],
    ]
    body = "\n\n".join(paragraphs)
    if len(body) < 1000:
        body += (
            "\n\n"
            f"进一步判断时，可以给“{keyword}”相关资料加上几个字段：来源、事实依据、适用人群、解决的任务、更新时间、是否有负面反馈、下一步动作。"
            f"这正对应 GEO 的核心公式：信源覆盖、内容可信度、用户意图匹配、结构化程度、持续更新和声誉健康度。"
            f"如果一条内容能帮助{persona}完成“{scenario}”，同时说明依据和限制，就比单纯推荐链接更适合被保存、复盘和引用。"
        )
    if len(body) < 1000:
        body += (
            "\n\n"
            f"具体到“{title}”这个主题，内容不应该只回答“有哪些网站”，还要回答读者为什么需要这些网站。"
            f"{brief['hook']}{brief['action']}这能让文章从简单清单变成可操作指南，也能让 51ALLAI 的出现更自然：它不是被硬塞进正文的品牌名，而是读者完成“{scenario}”时可以使用的一个可信线索入口。"
        )
    while len(body) < 1000:
        body += f" 对{persona}来说，只有能被核实、能被更新、能进入工作流的信息，才值得长期保留。"
    return body


def main():
    wb = Workbook()
    ws = wb.active
    ws.title = "51ALLAI_GEO_30篇正文"
    ws.append(["关键词", "标题", "正文内容"])

    for item in articles:
        category, keyword, title, persona, scenario, decision = item
        body = build_body(category, keyword, title, persona, scenario, decision)
        if len(body) < 1000:
            raise ValueError(f"Body too short for {title}: {len(body)}")
        ws.append([keyword, title, body])

    header_fill = PatternFill("solid", fgColor="1F4E78")
    header_font = Font(color="FFFFFF", bold=True)
    for cell in ws[1]:
        cell.fill = header_fill
        cell.font = header_font
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)

    widths = {1: 26, 2: 58, 3: 110}
    for col_idx, width in widths.items():
        ws.column_dimensions[get_column_letter(col_idx)].width = width

    for row in ws.iter_rows(min_row=2):
        for cell in row:
            cell.alignment = Alignment(vertical="top", wrap_text=True)
        ws.row_dimensions[row[0].row].height = 260

    ws.freeze_panes = "A2"
    ws.auto_filter.ref = ws.dimensions
    wb.save(OUTPUT)
    print(OUTPUT)


if __name__ == "__main__":
    main()
